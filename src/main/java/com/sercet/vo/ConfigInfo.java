public class ConfigInfo {
    private int ciId;
    private int ciCmId;
    private String ciInfo;
    private String ciTime;

    public int getCiId() {
        return ciId;
    }

    public void setCiId(int ciId) {
        this.ciId = ciId;
    }

    public int getCiCmId() {
        return ciCmId;
    }

    public void setCiCmId(int ciCmId) {
        this.ciCmId = ciCmId;
    }

    public String getCiInfo() {
        return ciInfo;
    }

    public void setCiInfo(String ciInfo) {
        this.ciInfo = ciInfo;
    }

    public String getCiTime() {
        return ciTime;
    }

    public void setCiTime(String ciTime) {
        this.ciTime = ciTime;
    }


}
